{
    g_seed = static_cast<uint64_t>(std::time(nullptr));

    for (int i = 1; i < argc; ++i) {
        if (std::strcmp(argv[i], "--verbose") == 0) {
            g_verbose = true;
        } else if (std::strcmp(argv[i], "--seed") == 0 && i+1 < argc) {
            g_seed = static_cast<uint64_t>(std::atoll(argv[++i]));
        } else if (std::strcmp(argv[i], "--rand-iters") == 0 && i+1 < argc) {
            g_rand_iters = std::atoi(argv[++i]);
        }
    }
    g_rng.seed(g_seed);

    std::printf("\n");
    std::printf("=============================================================\n");
    std::printf("  Deep Verification Suite\n");
    std::printf("  Static DB: %d bonds, %d angles, %d crystals,\n",
                N_BOND_REFS, N_ANGLE_REFS, N_CRYSTAL_REFS);
    std::printf("             %d LJ, %d diatomics, %d solvents, %d ions\n",
                N_LJ_REFS, N_DIATOMIC_REFS, N_SOLVENT_REFS, N_ION_REFS);
    std::printf("  Random:    seed=%llu  rand_iters=%d\n",
                (unsigned long long)g_seed, g_rand_iters);
#if HAS_DOWNLOADED_REFS
    std::printf("  Downloaded: %d bonds, %d crystals, %d diatomics, %d solvents\n",
                N_DOWNLOADED_BOND_REFS, N_DOWNLOADED_CRYSTAL_REFS,
                N_DOWNLOADED_DIATOMIC_REFS, N_DOWNLOADED_SOLVENT_REFS);
#else
    std::printf("  Downloaded: not present (run scripts/fetch_empirical.py)\n");
#endif
    std::printf("=============================================================\n");

    mkdir_p("verification/deep");

    auto model = create_lj_coulomb_model();
    ModelParams mp; mp.rc = 12.0;

    // Static suites
    suite_A(*model, mp);
    suite_B(*model, mp);
    suite_C(*model, mp);
    suite_D();
    suite_E();
    suite_F();
    suite_G(*model, mp);
    suite_H(*model);
    suite_N(*model, mp);
    suite_Q(*model);

    // Randomized suites
    suite_I(*model, mp);
    suite_J(*model);
    suite_K(*model);
    suite_L();
    suite_M(*model, mp);

    // Downloaded reference suite
    suite_P(*model, mp);

    // Summary
    int total = g_pass + g_fail + g_skip;
    std::printf("\n=============================================================\n");
    std::printf("  Deep Verification:  %d total,  %d passed,  %d failed,  %d skipped\n",
                total, g_pass, g_fail, g_skip);
    std::printf("  Seed: %llu  (re-run with --seed %llu to reproduce)\n",
                (unsigned long long)g_seed, (unsigned long long)g_seed);
    std::printf("=============================================================\n\n");

    // Write artifacts
    {
        std::ofstream f("verification/deep/report.txt");
        f << "Deep Verification Report\n";
        f << "========================\n\n";
        f << "Seed:    " << g_seed << "\n";
        f << "Passed:  " << g_pass << "\n";
        f << "Failed:  " << g_fail << "\n";
        f << "Skipped: " << g_skip << "\n\n";
        f << g_report.str();
    }
    {
        std::ofstream f("verification/deep/status.txt");
        f << (g_fail == 0 ? "PASS" : "FAIL") << "\n";
        f << "Seed:    " << g_seed << "\n";
        f << "Passed:  " << g_pass << "\n";
        f << "Failed:  " << g_fail << "\n";
        f << "Skipped: " << g_skip << "\n";
    }

    return g_fail > 0 ? 1 : 0;
}
