// ============================================================================
// M. NVE Energy Conservation
// ============================================================================

static void suite_M(IModel& model, const ModelParams& mp)
{
    g_report << "\n[Suite M] NVE Energy Conservation (seed=" << g_seed << ")\n";
    std::printf("\n--- Suite M: NVE Energy Conservation ---\n");

    // Test on Ar3, Ar4, Ar5 with randomized initial positions and velocities
    for (int N = 3; N <= 5; ++N) {
        vsepr::io::XYZMolecule mol;
        // Start near equilibrium spacing, add small random displacements
        auto lj_ar = find_lj("Ar");
        double r0 = lj_ar ? lj_ar->r_min : 3.82;
        for (int i = 0; i < N; ++i) {
            double angle = 2.0*M_PI*i/N;
            double x = r0 * std::cos(angle) + rnd_uniform(-0.1, 0.1);
            double y = r0 * std::sin(angle) + rnd_uniform(-0.1, 0.1);
            double z = rnd_uniform(-0.2, 0.2);
            mol.atoms.emplace_back("Ar", x, y, z);
        }
        State s = parse(mol);

        // Assign random velocities ~ Maxwell-Boltzmann at 100 K
        // k_B = 1.987e-3 kcal/(mol·K); mass Ar = 39.948 amu
        // v_rms = sqrt(3 k_B T / m) in kcal^0.5/(mol^0.5·amu^0.5)
        // In AKMA: 1 amu·(A/fs)^2 = 1 kcal/mol -> v in A/fs
        double kB_kcal = 1.987204e-3;  // kcal/(mol·K)
        double m_Ar    = 39.948;
        double T_K     = 100.0;
        double sigma_v = std::sqrt(kB_kcal * T_K / m_Ar);
        s.V.resize(N);
        std::normal_distribution<double> vnorm(0.0, sigma_v);
        for (int i = 0; i < N; ++i)
            s.V[i] = {vnorm(g_rng), vnorm(g_rng), vnorm(g_rng)};

        // Evaluate initial energy
        ModelParams mpr = mp; mpr.rc = 14.0;
        model.eval(s, mpr);
        double KE0 = 0;
        for (int i = 0; i < N; ++i) {
            auto& v = s.V[i];
            KE0 += 0.5 * m_Ar * (v.x*v.x + v.y*v.y + v.z*v.z);
        }
        double E0 = s.E.total() + KE0;

        // 200 NVE velocity-Verlet steps
        double dt = 0.001; // fs
        double max_drift = 0.0;
        bool   finite_ok = true;
        for (int step = 0; step < 200; ++step) {
            // half kick
            for (int i = 0; i < N; ++i) {
                s.V[i].x += 0.5*dt * s.F[i].x / m_Ar;
                s.V[i].y += 0.5*dt * s.F[i].y / m_Ar;
                s.V[i].z += 0.5*dt * s.F[i].z / m_Ar;
            }
            // drift
            for (int i = 0; i < N; ++i) {
                s.X[i].x += dt * s.V[i].x;
                s.X[i].y += dt * s.V[i].y;
                s.X[i].z += dt * s.V[i].z;
            }
            // re-eval forces
            s.F.assign(N, {0,0,0}); s.E = {};
            model.eval(s, mpr);
            // second half kick
            for (int i = 0; i < N; ++i) {
                s.V[i].x += 0.5*dt * s.F[i].x / m_Ar;
                s.V[i].y += 0.5*dt * s.F[i].y / m_Ar;
                s.V[i].z += 0.5*dt * s.F[i].z / m_Ar;
            }
            // KE
            double KE = 0;
            for (int i = 0; i < N; ++i) {
                auto& v = s.V[i];
                KE += 0.5 * m_Ar * (v.x*v.x + v.y*v.y + v.z*v.z);
            }
            double E_now = s.E.total() + KE;
            if (!std::isfinite(E_now)) { finite_ok = false; break; }
            double drift = std::abs(E_now - E0) / (std::abs(E0) + 1e-12);
            if (drift > max_drift) max_drift = drift;
        }

        std::string tag = "Ar" + std::to_string(N) + " NVE";
        check(finite_ok, tag + ": energy finite");
        check(max_drift < 1e-3, tag + ": energy conserved",
              "max_drift=" + std::to_string(max_drift));
        if (g_verbose)
            std::printf("    Ar%d: E0=%.4f  max_drift=%.2e\n", N, E0, max_drift);
    }
}

// ============================================================================
// P. Downloaded References (PubChem + NIST)
// ============================================================================

static void suite_P(IModel& model, const ModelParams& mp)
{
    (void)model; (void)mp;  // used only when HAS_DOWNLOADED_REFS
    g_report << "\n[Suite P] Downloaded Reference Verification\n";
    std::printf("\n--- Suite P: Downloaded References ---\n");

#if HAS_DOWNLOADED_REFS
    std::printf("  %d bond refs, %d crystal refs, %d diatomic refs, %d solvent refs\n",
                N_DOWNLOADED_BOND_REFS, N_DOWNLOADED_CRYSTAL_REFS,
                N_DOWNLOADED_DIATOMIC_REFS, N_DOWNLOADED_SOLVENT_REFS);

    // P1: Verify all downloaded bond lengths are physically sane
    for (int i = 0; i < N_DOWNLOADED_BOND_REFS; ++i) {
        const auto& b = DOWNLOADED_BOND_REFS[i];
        check(b.r_eq > 0.4 && b.r_eq < 5.0,
              std::string("downloaded bond ") + b.formula + " " + b.bond + ": r in [0.4,5.0]A",
              "r=" + std::to_string(b.r_eq));
    }

    // P2: Verify all downloaded diatomic r_e match our BOND_REFS within tolerance
    int matched = 0;
    for (int i = 0; i < N_DOWNLOADED_DIATOMIC_REFS; ++i) {
        const auto& d = DOWNLOADED_DIATOMIC_REFS[i];
        // Find matching entry in BOND_REFS
        for (int j = 0; j < N_BOND_REFS; ++j) {
            if (std::string(BOND_REFS[j].formula) == d.formula) {
                double err = std::abs(BOND_REFS[j].r_eq - d.r_eq);
                check(err < 0.02,
                      std::string("diatomic cross-check ") + d.formula + " " + d.bond,
                      "hardcoded=" + std::to_string(BOND_REFS[j].r_eq).substr(0,6) +
                      " downloaded=" + std::to_string(d.r_eq).substr(0,6));
                ++matched;
                break;
            }
        }
    }

    // P3: Solvent dielectric Coulomb ratio check for each downloaded solvent
    vsepr::io::XYZMolecule m_ion;
    m_ion.atoms.emplace_back("Na", 0.0, 0.0, 0.0);
    m_ion.atoms.emplace_back("Cl", 5.0, 0.0, 0.0);
    ModelParams mp_vac; mp_vac.rc = 20.0;
    mp_vac.env = EnvironmentContext::near_vacuum();
    State s_vac = parse(m_ion); s_vac.Q[0] = +1.0; s_vac.Q[1] = -1.0;
    model.eval(s_vac, mp_vac);
    double U_vac = s_vac.E.UCoul;

    for (int i = 0; i < N_DOWNLOADED_SOLVENT_REFS; ++i) {
        const auto& sv = DOWNLOADED_SOLVENT_REFS[i];
        if (sv.dielectric < 1.5) continue;
        ModelParams mps; mps.rc = 20.0;
        mps.env = EnvironmentContext::solution(sv.dielectric);
        State ss = parse(m_ion); ss.Q[0]=+1.0; ss.Q[1]=-1.0;
        model.eval(ss, mps);
        double ratio = U_vac / ss.E.UCoul;
        double err = std::abs(ratio - sv.dielectric) / sv.dielectric;
        check(err < 1e-4,
              std::string("downloaded solvent ") + sv.name + " screening",
              "eps=" + std::to_string(sv.dielectric).substr(0,6) +
              " ratio=" + std::to_string(ratio).substr(0,6));
    }

#else
    skip("downloaded bond refs",     "run scripts/fetch_empirical.py first");
    skip("downloaded diatomic refs", "run scripts/fetch_empirical.py first");
    skip("downloaded solvent refs",  "run scripts/fetch_empirical.py first");
#endif
}

