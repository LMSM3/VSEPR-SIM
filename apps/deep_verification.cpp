/**
 * deep_verification.cpp
 * ---------------------
 * Automated continuous deep verification suite.
 *
 * Static suites (empirical database):
 *   A. LJ parameter audit (42 UFF elements vs Rappe 1992)
 *   B. Homonuclear dimer sweeps (energy curves, r_min, smoothness)
 *   C. Heteronuclear ion pair Coulomb + force consistency
 *   D. Crystal geometry vs Wyckoff (30 structures)
 *   E. Molecule bond detection (9 molecules)
 *   F. Composite force-energy consistency (H2O, NH3, CH4, Ar2)
 *   G. Noble gas cluster FIRE — two-layer: descent + convergence quality
 *   H. Full solvent dielectric sweep (19 CRC_104 solvents)
 *   N. Restoring-force scan: homonuclear dimers He2--Xe2 (compression+extension)
 *   Q. Relaxed crystal stability: 2x2x2 supercell FIRE + nn/overlap guard
 *
 * Randomized suites (different result each run, seed logged):
 *   I. Random atom-pair LJ force-energy consistency (N_RAND=100)
 *   J. Random Coulomb law 1/r verification (N_RAND=80)
 *   K. Random dielectric ratio check (N_RAND=60)
 *   L. Random crystal supercell atom-count verification
 *   M. NVE energy conservation on random Ar clusters
 *
 * Downloaded refs suite:
 *   P. Verify bond lengths against PubChem/NIST downloaded_refs.hpp
 *      (skipped if verification/downloaded_refs.hpp not present)
 *
 * Usage:
 *   ./deep_verification [--verbose] [--seed N] [--rand-iters N]
 *   Exit code 0 = all pass, 1 = failures present
 *   Writes verification/deep/report.txt and status.txt
 */

