LocalUFFReferenceProvider::LocalUFFReferenceProvider() {
	load_baseline_();
}

void LocalUFFReferenceProvider::load_baseline_() {
	for (const auto& row : k_rappe1992) {
		UFFEntry e;
		e.atom_type          = row.atom_type;
		e.element            = row.element;
		e.coordination_number = row.coordination;
		e.geometry_tag       = row.geometry;
		e.r1     = row.r1;
		e.theta0 = row.theta0;
		e.x1     = row.x1;
		e.D1     = row.D1;
		e.zeta   = row.zeta;
		e.Z1     = row.Z1;
		e.Vi     = row.Vi;
		e.Uj     = row.Uj;
		e.Xi     = row.Xi;
		e.Hard   = row.Hard;
		e.Radius = row.Radius;
		e.confidence  = ParamConfidence::Published;
		e.source_id   = "rappe1992";
		e.source_note = "Rappe et al., JACS 1992, 114, 10024-10035, Table 1";
		table_.insert(std::move(e));
	}
}

std::optional<UFFEntry> LocalUFFReferenceProvider::lookup(
	const std::string& atom_type) const
{
	return table_.lookup(atom_type);
}

std::vector<std::string> LocalUFFReferenceProvider::known_types() const {
	auto entries = table_.all_entries();
	std::vector<std::string> types;
	types.reserve(entries.size());
	for (const auto& e : entries) types.push_back(e.atom_type);
	return types;
}

